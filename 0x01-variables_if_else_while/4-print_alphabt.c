#include <stdio.h>

/**
 * main - entry point
 *
 * Return: 0 Success
 */
int main(void)
{
	char alph;

	alph = 'a';
	while (alph <= 'z')
	{
		if (alph == 'e')
		{
			alpha++;
		}
		else if (alph == 'q')
		{
			alpha++;
		}
		else 
		{
			putchar(alph);
			alph++;
		}
	}
	putchar('\n');
	return (0);
}
